package in.com.oops;

public class Switchcase {
	public static void main(String[] args) {
		String value="div";
		switch (value) {
		case "add":
		System.out.println("case add");
		break;
		case "div":
			System.out.println("case div");
			break;
			default:
				System.out.println("nothingggg");
				break;
		
		}
	}

}
