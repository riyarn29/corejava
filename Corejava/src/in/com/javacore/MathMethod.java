package in.com.javacore;

public class MathMethod {
public static void main(String[] args) {
	
	//max no.
	System.out.println("max ="+Math.max(0.1, 2.1));
	
	//min no.
	System.out.println("min="+Math.min(0.01, -1.1));
	
	//absolute
	System.out.println("absolute value="+Math.abs(25));
	
	//exp
	System.out.println("exp value="+Math.exp(10));
	
	//random no.
	System.out.println("random="+Math.random());
	
	//square root
	System.out.println("square root="+Math.sqrt(36));
}
}
